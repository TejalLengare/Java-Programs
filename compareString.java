//Compare StringsS

public class compareString {
    public static void main(String[] args) {
        String s1 ="Ram";
        String s2="Varun";

        //Compare String using CompareTo Method
        if((s1.compareTo(s2))==0){
            System.out.println("Strings are Equal");
        }else{
            System.out.println("Strings are unequal");
        }
    }
}




